    private void IngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarActionPerformed

        FileReader fr = null;
        try {

            int nLineas = 0;
            int i = 0;
            String []usuarios;
            String linea;

            archivoTxt = new Scanner(new File(Ruta1));
            File f = new File(Ruta1);
            fr = new FileReader(f);
            BufferedReader br = new BufferedReader(fr);

            try {
                while((linea = br.readLine()) != null){
                    nLineas++;
                }
            } catch (IOException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }

            usuarios = new String[nLineas]; //tama침o del array

            while(archivoTxt.hasNext()){
                usuarios[i++] = archivoTxt.nextLine(); // almacena cada linea del txt en una posicion diferente en el array
            }

            Usuario = txtUser.getText();
            Password = txtPwd.getText();
            intentos++;

            ValidarInicioDeSesion s = new ValidarInicioDeSesion();
            s.validarUsuario(usuarios, Usuario, Password, intentos, this);

        } catch (FileNotFoundException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                fr.close();
            } catch (IOException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_IngresarActionPerformed

